destino:-nl,
		write('ingrese el clima del departamento 1. calido 2. frio'),
		nl,
		read(CLIMA),
		nl,
		buscardep(CLIMA),
		nl,
		write('seleccione un departamento'),
		nl,
		read(DEPARTAMENTO),
		nl,
		bucarhotel(DEPARTAMENTO),
		nl,
		write('Posee Auto: s/n'),
		nl,
		read(CARRO),
		nl,
		write('seleccione hotel'),
		nl,
