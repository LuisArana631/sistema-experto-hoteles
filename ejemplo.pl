					(CA =='s'->T is O;CA =='n'->T is O*2),
					R is A + B + T,nl,
					(R == 0 ->write('no hay resultados');
					P >= R->hotel(X,Z),write(Z),write(' '),write('Q'),write(R),fail;
					P < R->write('no hay resultados no alcanza el presupeusto')),nl.

