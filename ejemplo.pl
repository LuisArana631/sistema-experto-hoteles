		nl.

buscardep(X):- clima(Y,X),departamento(Y,Z),write(Z),nl,fail;nl.
bucarhotel(X):- departamento(Y,X),lugarhotel(W,Y),hotel(W,Z),write(Z),nl,fail;nl.
datoshotel(X,J):- hotel(W,X),direccion(W,A),direccionb(W,DA),write('Distancia '),write(DA),write(A),nl,
				estrellas(W,B),write('Estrellas '),write(B),nl,
				hsimple(W,C),write('Habitacion simple '),write('Q'),write(C),nl,
				hdoble(W,D),write('Habitacion doble '),write('Q'),write(D),nl,
				comida(W,E),write('Precio comida '),write('Q'),write(E),nl,
				costoida(W,F),write('Precio viaje '),
				(J=='s'->write('Q'),write(F);J=='n'->R is F*2,write('Q'),write(R)),nl.

nomdep(X):- departamento(Y,X),write(Y).

